import { Schema,model } from "mongoose";

const RoleSchema = new Schema({
    role:{
        type:String,
        require:true
    },
    isActive:{
        type:Boolean,
        require:false,
        default:false
    },
    isCreated:{
        type:Boolean,
        require:false,
        default:false
    },
    isUpdated:{
        type:Boolean,
        require:false,
        default:false
    },
    createdId:{
        type:String,
        require:false
    },
    updatedId:{
        type:String,
        require:false
    }
},{timestamps:true});

const Role = model('Role',RoleSchema);
export default Role;